function світлофор(хвилина) {
    // Визначаємо, в якому 5-хвилинному циклі знаходиться час
    let цикл = хвилина % 5;

    // Перевіряємо кольор світлофора в залежності від циклу
    if (цикл < 3) {
        return "Зелений";
    } else {
        return "Червоний";
    }
}

// Використовуйте функцію світлофор з введеним числом хвилин
let введенаХвилина = parseInt(prompt("Введіть число хвилин, щоб дізнатися кольор світлофора:"), 10);

// Застосовуємо функцію світлофор до введеної хвилини
let кольорСвітлофора = світлофор(введенаХвилина);

// Виводимо результат на екран
alert("Зараз горить " + кольорСвітлофора + " сигнал.");